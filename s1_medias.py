#Descrição: Programa que calcula a média aritmética de quatro notas.

#Receber as quatros notas na entrada do programa.
nota1 = float(input ('Digite a primeira nota:'))
nota2 = float(input ('Digite a segunda nota:'))
nota3 = float(input ('Digite a terceira nota:'))
nota4 = float(input ('Digite a quarta nota:'))

#Calcular a média aritmética das 4 notas e armazenar na variável
mediaAritmetica = (nota1 + nota2 + nota3 + nota4)/4

#Saída de dados:
print('A média aritmética é',mediaAritmetica)
